public class week_1_primitive_data_type {

    static int Intval;
    static float Floatval;
    static byte Byteval;
    static long Longval;
    static boolean Booleanval;
    static char Characterval;
    static double Doubleval;
    static short Shortval;
    public static void main(String[] args) {
        System.out.println("Default int value: " + Intval);
        System.out.println("Default float value: " + Floatval);
        System.out.println("Default byte value: " + Byteval);
        System.out.println("Default long value: " + Longval);
        System.out.println("Default boolean value: " + Booleanval);
        System.out.println("Default char value: " + Characterval);
        System.out.println("Default double value: " + Doubleval);
        System.out.println("Default short value: " + Shortval);
    }
}